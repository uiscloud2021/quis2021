function CargarSujetoRec(){
    empresa_id=$('#empresa_id').val();
    proyecto_id=$('#proyecto_id').val();
    $.ajax({
        url: "/eq-sc/cargarsujeto_reclutamiento",
        method:'POST',
        dataType: 'json',
        data:{empresa_id:empresa_id, proyecto_id:proyecto_id, _token:$('input[name="_token"]').val()},
        success:function(resp){
            var sujetos = JSON.parse(resp);
            $('#r107').empty();
            $('#r123').empty();
            $('#r138').empty();
            $('#r151').empty();
            $('#r160').empty();

            $("#r107").append("<option selected value=''>Seleccione...</option>");
            $.each(sujetos, function() {
                $('#r107').append("<option value='"+this.id+"'>"+this.r81+"</option>");
            });
            $("#r123").append("<option selected value=''>Seleccione...</option>");
            $.each(sujetos, function() {
                $('#r123').append("<option value='"+this.id+"'>"+this.r81+"</option>");
            });
            $("#r138").append("<option selected value=''>Seleccione...</option>");
            $.each(sujetos, function() {
                $('#r138').append("<option value='"+this.id+"'>"+this.r81+"</option>");
            });
            $("#r151").append("<option selected value=''>Seleccione...</option>");
            $.each(sujetos, function() {
                $('#r151').append("<option value='"+this.id+"'>"+this.r81+"</option>");
            });
            $("#r160").append("<option selected value=''>Seleccione...</option>");
            $.each(sujetos, function() {
                $('#r160').append("<option value='"+this.id+"'>"+this.r81+"</option>");
            });
        }
    });
}

