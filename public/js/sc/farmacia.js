function GuardarFarmacia(){
    $('#overlay').show();
    form=$('#formcreate_farmacia').serialize();
    $.ajax({
        url: "/eq-sc/create_farmacia",
        type:'post',
        data:form,
        success:function(resp){
            $('#farmacia_id').val(resp);
            $('#overlay').hide();
        }
    });
}



