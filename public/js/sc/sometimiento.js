function list_equiposom(){
    empresa_id=$('#empresa_id').val();
    proyecto_id=$('#proyecto_id').val();
    var list = $('#tbl_equiposom').DataTable({
        dom: 'T<"clear">lfrtip',
        "processing": true,
        "serverSide": true,
        destroy: true,
        "lengthMenu": [[5, 10, 50, -1], [5, 10, 50, "Todos"]],
        "ajax":{
            "url": "/eq-sc/list_equiposom",
            "method": "POST",
            "data": {
                empresa_id:empresa_id,
                proyecto_id:proyecto_id,
                _token:$('input[name="_token"]').val()
            },
            
        },
        "columns": [
            {"data": 'nombre'},
            {"data": 'rol'},
            {"data": 'edit'},
            {"data": 'delete'},
        ],
        "language": espanol
    });
}

$('#formcreate_equiposom').on('submit', function(e) {
    e.preventDefault();
    var formData = new FormData(this);
    formData.append('_token', $('input[name=_token]').val());
    no3=$('#s3').val();
    id=$('#sometimientoid_equiposom').val();
    empresa_id=$('#empresaid_equiposom').val();
    proyecto_id=$('#proyectoid_equiposom').val();
    if(no3!=""){
        $.ajax({
            url: "/eq-sc/create_equiposom",
            type:'post',
            data:formData,
            cache:false,
            contentType: false,
            processData: false,
            beforeSend:function(){
                $('#btnGEquipoSom').hide();
            },
            success:function(resp){
                if(resp == "guardado"){
                    $('#createEquipoSomModal').modal('hide');
                    toastr.success('El miembro del equipo fue guardado correctamente', 'Guardar equipo', {timeOut:3000});
                    if(id==""){
                        location.href=proyecto_id+"/show";
                    }else{
                        $('#btnGEquipoSom').show();
                        list_equiposom();
                    }
                }else{
                    $('#btnGEquipoSom').show();
                    toastr.warning('El miembro del equipo ya se encuentra dado de alta', 'Guardar equipo', {timeOut:3000});
                }
            }
        });
    }else{
        alert("No puede estar el miembro del equipo vacío");
    }
});

