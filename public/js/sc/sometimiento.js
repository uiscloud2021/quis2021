    $('#s9_no').prop('checked', false);
    $('#s10_si').prop('checked', false);
    $('#s10_no').prop('checked', false);
    $('#s11_si').prop('checked', false);
    $('#s11_no').prop('checked', false);
    $('#s12_si').prop('checked', false);
    $('#s12_no').prop('checked', false);
