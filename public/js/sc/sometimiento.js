$('#formcreate_equipo').on('submit', function(e) {
    e.preventDefault();
    var formData = new FormData(this);
    formData.append('_token', $('input[name=_token]').val());
    no3=$('#no3').val();
    id=$('#sometimientoid_equipo').val();
    empresa_id=$('#empresaid_equipo').val();
    proyecto_id=$('#proyectoid_equipo').val();
    if(no3!=""){
        $.ajax({
            url: "/sometimiento/create_equipo",
            type:'post',
            data:formData,
            cache:false,
            contentType: false,
            processData: false,
            beforeSend:function(){
                $('#btnGEquipo').hide();
            },
            success:function(resp){
                if(resp == "guardado"){
                    $('#createEquipoModal').modal('hide');
                    toastr.success('El miembro del equipo fue guardado correctamente', 'Guardar equipo', {timeOut:3000});
                    if(id==""){
                        location.href=proyecto_id+"/edit";
                    }else{
                        $('#btnGEquipo').show();
                        list_equipo();
                    }
                }else{
                    $('#btnGEquipo').show();
                    toastr.warning('El miembro del equipo ya se encuentra dado de alta', 'Guardar equipo', {timeOut:3000});
                }
            }
        });
    }else{
        alert("No puede estar el nombre del miembro del equipo vacío");
    }
});

