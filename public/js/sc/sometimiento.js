$('#formcreate_respuesta').on('submit', function(e) {
    e.preventDefault();
    var formData = new FormData(this);
    formData.append('_token', $('input[name=_token]').val());
    no23=$('#no23').val();
    id=$('#sometimientoid_respuesta').val();
    empresa_id=$('#empresaid_respuesta').val();
    proyecto_id=$('#proyectoid_respuesta').val();
    if(no13!=""){
        $.ajax({
            url: "/sometimiento/create_respuesta",
            type:'post',
            data:formData,
            cache:false,
            contentType: false,
            processData: false,
            beforeSend:function(){
                $('#btnGRespuesta').hide();
            },
            success:function(resp){
                if(resp == "guardado"){
                    $('#createRespuestaModal').modal('hide');
                    toastr.success('La respuesta fue guardada correctamente', 'Guardar respuesta', {timeOut:3000});
                    if(id==""){
                        location.href=proyecto_id+"/edit";
                    }else{
                        $('#btnGRespuesta').show();
                        list_respuesta();
                    }
                }else{
                    $('#btnGRespuesta').show();
                    toastr.warning('La respuesta ya se encuentra dada de alta', 'Guardar respuesta', {timeOut:3000});
                }
            }
        });
    }else{
        alert("No puede estar la fecha de respuesta vacía");
    }
});

function edit_respuesta(id_respuesta){
    $.ajax({
        url: "/sometimiento/edit_respuesta",
        method:'POST',
        dataType: 'json',
        data:{id_respuesta:id_respuesta, _token:$('input[name="_token"]').val()},
        success:function(data){
            var posts = JSON.parse(data);
            $.each(posts, function() {
                $('#empresaid_respuesta').val(this.empresa_id);
                $('#sometimientoid_respuesta').val(this.sometimiento_id);
                $('#proyectoid_respuesta').val(this.proyecto_id);
                $('#id_respuesta').val(id_respuesta);
                $('#no23').val(this.no23);
                $('#no24').val(this.no24);
                $('#createRespuestaModal').modal('toggle');
            });
        }
    });
}

